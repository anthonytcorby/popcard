  const RTL = new Set(['ar']);

  function apply(lang) {
    const dict = T[lang] || T.en;
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const v = dict[el.dataset.i18n];
      if (v != null) el.textContent = v;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      const v = dict[el.dataset.i18nPlaceholder];
      if (v != null) el.setAttribute('placeholder', v);
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
      const v = dict[el.dataset.i18nAriaLabel];
      if (v != null) el.setAttribute('aria-label', v);
    });
    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
      const v = dict[el.dataset.i18nTitle];
      if (v != null) el.setAttribute('title', v);
    });
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', RTL.has(lang) ? 'rtl' : 'ltr');
  }

  window.PopcardI18n = { apply, languages: Object.keys(T) };

  // Initial apply once DOM is ready.
  const init = () => {
    const lang = window.PopcardLang?.current || 'en';
    apply(lang);
  };
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  // Re-apply when the user changes language (event dispatched by lang-picker.js).
  window.addEventListener('popcard-language-change', (e) => {
    const lang = e.detail?.language || 'en';
    apply(lang);
  });
